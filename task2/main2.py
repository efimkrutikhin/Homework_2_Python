def find_numbers(S, P):
    # Инициализируем переменные для хранения найденных чисел
    X, Y = None, None

    # Перебираем возможные значения X от 1 до S (включительно)
    for X in range(1, S + 1):
        # Вычисляем значение Y, зная S и X
        Y = S - X

        # Проверяем, что произведение X и Y равно P, и оба числа не превышают 1000
        if X * Y == P and X <= 1000 and Y <= 1000:
            return X, Y

    # Если не удалось найти подходящие значения X и Y, возвращаем None
    return None, None

# Запросим у Пети значения суммы S и произведения P
S = int(input("Введите сумму S: "))
P = int(input("Введите произведение P: "))

# Ищем числа X и Y, соответствующие заданным сумме S и произведению P
X, Y = find_numbers(S, P)

# Выводим результат
if X is None or Y is None:
    print("Невозможно найти подходящие числа X и Y.")
else:
    print(f"Отгаданные числа: X = {X}, Y = {Y}")
